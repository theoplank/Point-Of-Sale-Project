    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        
        String userName = jTextField1.getText();
        String password = jTextField2.getText();
        int uLen = userName.length();
        int pLen = password.length();
        PreparedStatement ps;
        ResultSet rs;
        int userPK;


        try {
            if (uLen < 6 || pLen <6){
                JOptionPane.showMessageDialog(null, "invalid, username and password must be 6 or more characters long");
            }
            else {
                ps = classes.database.getConnection().prepareStatement("SELECT `username`, `password` , `user_pk` PK FROM `users` WHERE `username` = ? AND `password` = ?");
                ps.setString(1, jTextField1.getText());
                ps.setString(2, jTextField2.getText());
                rs = ps.executeQuery();
                
                
                if (rs.next()){
                    
                    userPK = rs.getInt("PK");
                    menuScreen menu = new menuScreen(); 
                    //menu.sessionID = currentSession;
                    menu.setDefaultCloseOperation(menuScreen.EXIT_ON_CLOSE);
                    menu.pack();
                    menu.setVisible(true);
                    int currentSession = classes.userSessions.insertUserSession(userPK);
                    System.out.println(Integer.toString(currentSession));
                    menu.sessionID = currentSession;
                    boolean validity = classes.userSessions.isSessionValid(currentSession);
                    System.out.println(validity);
                    menu.prim = userPK;
                    this.dispose();
                    
                }
                else{
                    JOptionPane.showMessageDialog(null, "incorrect username or password");
                }
            }

        }  
        catch(SQLException ex){
            Logger.getLogger(loginScreen.class.getName()).log(Level.SEVERE, null, ex);
        }

        
        

    }//GEN-LAST:event_loginButtonActionPerformed

